    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request, $post_name)
    {

        $post = Post::where('post_name', $post_name)->first();
   
        $comment = new Comment(); 
        $comment->comment_name = $request->input('comment_name');
        $comment->comment_email = $request->input('comment_email');
        $comment->comment_content = $request->input('comment_content');
        $comment->post()->associate($post);

        $comment->save();

        Session::flash('success', 'Comment was added');

        return redirect()->route('showArticle',[$post_name]);
    }

